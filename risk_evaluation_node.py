#!/usr/bin/env python3

"""
risk_evaluation_node.py

Risk Evaluation Node
Digital Twin-Assisted Risk-Aware MPC

Author : Majdi Hassan et al.

Description
-----------
This node evaluates the cumulative horizon collision risk using the
predicted obstacle positions generated by the Obstacle Prediction Node.

Functions
---------
1. Receive Digital Twin robot state (/digital_twin/state)
2. Receive predicted obstacle positions (/predicted_obstacles)
3. Compute cumulative Gaussian collision risk (Eqs. 18-19)
4. Publish collision risk (/collision_risk)
"""

import math
import rclpy
from rclpy.node import Node
from geometry_msgs.msg import Pose2D, PoseArray
from std_msgs.msg import Float32


class RiskEvaluationNode(Node):

    # Constructor

    def __init__(self):
        super().__init__("risk_evaluation_node")


        # Risk Parameters (Table I Alignment)
        self.Ts = 0.10             # Sampling Period (Ts = 0.10 s)
        self.sigma = 0.35          # Risk Sensitivity Parameter (\sigma = 0.35)
        self.risk_threshold = 1.0  # Risk Threshold Boundary

        # Topics
        self.state_topic = "/digital_twin/state"
        self.prediction_topic = "/predicted_obstacles"
        self.risk_topic = "/collision_risk"

        # State Variables
        self.robot_x = 0.0
        self.robot_y = 0.0
        self.robot_theta = 0.0

        self.predicted_obstacles = []

        # Risk Metrics Buffers
        self.total_risk = 0.0
        self.maximum_risk = 0.0
        self.minimum_distance = float("inf")

        # Subscribers
        self.create_subscription(
            Pose2D,
            self.state_topic,
            self.robot_callback,
            10
        )

        self.create_subscription(
            PoseArray,
            self.prediction_topic,
            self.prediction_callback,
            10
        )

        # Publishers
        self.risk_pub = self.create_publisher(
            Float32,
            self.risk_topic,
            10
        )

        # Main Timer Loop (10 Hz / Ts = 0.10 s)
        self.timer = self.create_timer(
            self.Ts,
            self.compute_risk
        )

        # Startup Message
        self.get_logger().info("Risk Evaluation Node Started.")

    # Robot State Callback

    def robot_callback(self, msg: Pose2D):
        self.robot_x = msg.x
        self.robot_y = msg.y
        self.robot_theta = msg.theta

    # Predicted Obstacles Callback

    def prediction_callback(self, msg: PoseArray):
        self.predicted_obstacles = list(msg.poses)

    # Compute Cumulative Collision Risk (Eqs. 18-19)

    def compute_risk(self):
        # Empty array if no obstacles detected
        if len(self.predicted_obstacles) == 0:
            self.total_risk = 0.0
            self.maximum_risk = 0.0
            self.minimum_distance = float("inf")

            msg = Float32()
            msg.data = 0.0
            self.risk_pub.publish(msg)
            return

        self.total_risk = 0.0
        self.maximum_risk = 0.0
        self.minimum_distance = float("inf")

        # Sum Gaussian risk across predicted obstacle horizon (Eq. 18-19)
        for obstacle in self.predicted_obstacles:
            dx = self.robot_x - obstacle.position.x
            dy = self.robot_y - obstacle.position.y
            distance = math.sqrt(dx * dx + dy * dy)

            if distance < self.minimum_distance:
                self.minimum_distance = distance

            # Gaussian Collision Risk Potential (Eq. 18)
            risk = math.exp(-(distance ** 2) / (self.sigma ** 2))

            # Cumulative Risk Summation (Eq. 19)
            self.total_risk += risk

            if risk > self.maximum_risk:
                self.maximum_risk = risk

        # Publish Cumulative Horizon Collision Risk
        msg = Float32()
        msg.data = float(self.total_risk)
        self.risk_pub.publish(msg)

        self.print_diagnostics()

    # Diagnostics Logger

    def print_diagnostics(self):
        self.get_logger().debug(f"Cumulative Horizon Risk : {self.total_risk:.4f}")
        self.get_logger().debug(f"Maximum Point Risk      : {self.maximum_risk:.4f}")
        self.get_logger().debug(f"Minimum Distance        : {self.minimum_distance:.3f} m")

        if self.total_risk >= self.risk_threshold:
            self.get_logger().warn("High cumulative collision risk detected.")


# Main Entry Point

def main(args=None):
    rclpy.init(args=args)
    node = RiskEvaluationNode()

    try:
        rclpy.spin(node)
    except KeyboardInterrupt:
        node.get_logger().info("Risk Evaluation Node Stopped.")
    finally:
        node.destroy_node()
        rclpy.shutdown()


if __name__ == "__main__":
    main()